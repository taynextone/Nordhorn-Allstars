function drawSprite(spriteData, x, y, scale = 1) {
  for (let row = 0; row < spriteData.length; row++) {
    for (let col = 0; col < spriteData[row].length; col++) {
      const colorIdx = spriteData[row][col];
      if (colorIdx === 0) continue; // transparent
      ctx.fillStyle = SpritePalette[colorIdx];
      ctx.fillRect(
        Math.floor(x + col * scale),
        Math.floor(y + row * scale),
        scale,
        scale
      );
    }
  }
}

function getPlayerSprite(gender, direction, frame) {
  const set = gender === 'boy' ? Sprites.playerBoy : Sprites.playerGirl;
  const dir = set[direction] || set.down;
  return dir[frame % dir.length];
}
